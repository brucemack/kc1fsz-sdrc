static unsigned incAndWrap(unsigned i, unsigned len) {
    if (i == len - 1) 
        return 0;
    else 
        return i + 1;
}

static unsigned decAndWrap(unsigned i, unsigned len) {
    if (i == 0) 
        return len - 1;
    else 
        return i - 1;
}

